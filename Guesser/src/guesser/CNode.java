package guesser; 

public class CNode {

    public CNode left;
    public CNode right;
    public String data;

    

    /**
     * Constructor CNode
     */    
    public CNode () {

        left = null;
        right = null;
        data = null;

    }


    /**
     * Constructor CNode
     */    
    public CNode (String s) {

        left = null;
        right = null;
        data = s;

    }




}
